package no.hib.dat100.Oppgave_1;

public class Oppgave_1 {
	public static void main(String[] args){
		// Definerer variablene bredde, lengde og areal
		int bredde, lengde, areal;
		
		// Rektangel nr.1
		bredde = 3;
		lengde = 5;
		areal = lengde * bredde;
		
		System.out.println(
				"1: Areal av et rektangel med bredde " + bredde +
				" og lengde " + lengde +
				" er " + areal);
		
		// Rektangel nr.2
		bredde = 7;
		lengde = 3;
		areal = lengde * bredde;
		
		System.out.println(
				"2: Areal av et rektangel med bredde " + bredde +
				" og lengde " + lengde +
				" er " + areal);
		
		// Rektangel nr.3
		bredde = 4;
		lengde = 9;
		areal = lengde * bredde;
		
		System.out.println(
				"3: Areal av et rektangel med bredde " + bredde +
				" og lengde " + lengde +
				" er " + areal);
	
	}

}
